# Варіант №12
#
#   Є монотонна послідовність, в якій кожне натуральне число
#   k зустрічається рівно k разів: 1, 2, 2, 3, 3, 3, 4, 4, 4, 4, ....
#   За введеним натуральним числом n виведіть перші n членів цієї послідовності.
#
# Дзюба Владислав

n = int(input("Ввести натуральне число n: "))
k = 1
#          start,stop,step
for i in range(1, n+ 1):
    print(k, end=' ')
    if i == k * (k + 1) // 2:
        k += 1
